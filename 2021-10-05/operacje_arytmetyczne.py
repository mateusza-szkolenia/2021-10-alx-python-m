print("Dodawanie:", 2 + 2)
print("Odejmowanie:", 3 - 7)

print("Mnożenie:", 10 * 3)
print("Dzielenie '/':", 5 / 3)  # dzielenie rzeczywiste - wynikiem jest float
print("Dzielenie '//':", 7 // 3)  # dzielenie całkowitoliczbowe - ew. wartość po przecinku jest gubiona - wynikiem jest int
print("Modulo '%':", 7 % 3)  # reszta z dzielenia

print("Potęgowanie:", 2 ** 3)

print(2 + 2 * 2 ** 2)
print(((2 + 2) * 2) ** 2)
