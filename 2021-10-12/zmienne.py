podstawa = 10
wysokosc = 5

pi = 3.1415

# I pole trójkąta o podstawie 10 i wysokości 5
wiadomosc = "Pole trójkąta:"
print(wiadomosc, podstawa * wysokosc / 2)
# I pole koła o promieniu 7
r = 7
print("Pole koła:", pi * r ** 2)
# I pole trapezu o długości podstaw 3 i 9 oraz wysokości 6.5
print("Pole trapezu:", (3 + 9) * 6.5 / 2)
# I objętość kuli o promieniu 7/8
r = 7 / 8
print("Objętość kuli:", (4 / 3) * pi * r ** 3)
